      function changeToIDR(data) {
        const type = $(data).attr('id')
        var value = $(data).val().replace(/[^,\d]/g, '');
        if (value) {
          var rupiah = '';
          var valueArray = value.split(',');
          var sisa = valueArray[0].length % 3;
          var rupiah = valueArray[0].substr(0, sisa);
          var ribuan = valueArray[0].substr(sisa).match(/\d{3}/gi);

          if (ribuan) {
            var separator = sisa ? '.' : '';
            rupiah += separator + ribuan.join('.');
          }

          rupiah = valueArray[1] != undefined ? rupiah + ',' + valueArray[1] : rupiah;
          $(data).val('Rp ' + rupiah);

          let valNumber = value.replace(/[^,\d]/g, '');
          $(`input[name=${type}]`).val(valNumber)
        } else {
          $(data).val('');
        }

      }
